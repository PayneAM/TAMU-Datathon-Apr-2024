# Overview: While univariate time series analysis uses
# past values of a single series to predict its future,
# multi-series forecasting extends this by modeling
# multiple time series simultaneously. This approach
# leverages relationships across the series to enhance
# predictive accuracy.

# Your Challenge: Predict the number of passengers
# for 5 different trains over the next year. Your forecasts
# should account for potential interactions and
# dependencies between the different train lines.

# Dataset: You will use the ‘train.csv’ dataset, which
# includes records of passenger counts for five trains
# from 2013 to 2018.

# How to Turn It In: Check out
# ‘test_submission_example.csv’ for how you should
# format your prediction. Use the data in ‘test.csv’ to
